-- 5. Back-fill existing journey_nodes whose checklist is empty
UPDATE public.journey_nodes n
SET checklist = t.checklist
FROM public.journey_templates t
WHERE n.template_id = t.id
  AND (n.checklist IS NULL OR jsonb_array_length(n.checklist) = 0)
  AND jsonb_array_length(t.checklist) > 0;
