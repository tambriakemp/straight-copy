-- 3. Update seed trigger to copy checklist onto new journey nodes
CREATE OR REPLACE FUNCTION public.seed_journey_nodes_for_client()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO 'public'
AS $function$
BEGIN
  INSERT INTO public.journey_nodes (client_id, template_id, key, label, order_index, checklist)
  SELECT NEW.id, t.id, t.key, t.label, t.order_index, t.checklist
  FROM public.journey_templates t
  WHERE t.tier = NEW.tier
  ON CONFLICT (client_id, key) DO NOTHING;
  RETURN NEW;
END;
$function$;

CREATE OR REPLACE FUNCTION public.sync_journey_nodes_on_tier_change()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO 'public'
AS $function$
BEGIN
  IF NEW.tier IS DISTINCT FROM OLD.tier THEN
    INSERT INTO public.journey_nodes (client_id, template_id, key, label, order_index, checklist)
    SELECT NEW.id, t.id, t.key, t.label, t.order_index, t.checklist
    FROM public.journey_templates t
    WHERE t.tier = NEW.tier
    ON CONFLICT (client_id, key) DO NOTHING;
  END IF;
  RETURN NEW;
END;
$function$;

-- 4. Auto-complete + auto-advance trigger
CREATE OR REPLACE FUNCTION public.auto_complete_journey_node()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO 'public'
AS $function$
DECLARE
  total_items int;
  done_items int;
  next_node_id uuid;
BEGIN
  -- Only act when checklist actually changed
  IF NEW.checklist IS NOT DISTINCT FROM OLD.checklist THEN
    RETURN NEW;
  END IF;

  total_items := jsonb_array_length(COALESCE(NEW.checklist, '[]'::jsonb));
  IF total_items = 0 THEN
    RETURN NEW;
  END IF;

  SELECT count(*) INTO done_items
  FROM jsonb_array_elements(NEW.checklist) item
  WHERE COALESCE((item->>'done')::boolean, false) = true;

  -- All items done → mark complete + advance next pending node to in_progress
  IF done_items = total_items AND NEW.status <> 'complete' THEN
    NEW.status := 'complete';
    NEW.completed_at := COALESCE(NEW.completed_at, now());
    IF NEW.started_at IS NULL THEN NEW.started_at := now(); END IF;

    SELECT id INTO next_node_id
    FROM public.journey_nodes
    WHERE client_id = NEW.client_id
      AND order_index > NEW.order_index
      AND status = 'pending'
    ORDER BY order_index ASC
    LIMIT 1;

    IF next_node_id IS NOT NULL THEN
      UPDATE public.journey_nodes
      SET status = 'in_progress', started_at = COALESCE(started_at, now())
      WHERE id = next_node_id;
    END IF;
  END IF;

  RETURN NEW;
END;
$function$;

DROP TRIGGER IF EXISTS auto_complete_journey_node_trg ON public.journey_nodes;
CREATE TRIGGER auto_complete_journey_node_trg
BEFORE UPDATE ON public.journey_nodes
FOR EACH ROW EXECUTE FUNCTION public.auto_complete_journey_node();

